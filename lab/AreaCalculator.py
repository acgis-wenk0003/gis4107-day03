"""This module will calculate the area of a few different shapes."""

pi = 3.14159

def getCircleArea(radius):
    """getCircleArea(number) -> (number)

    Return the area of a circle with radius(number)"""
    area = pi*(radius ** 2)

    return area




def getSquareArea(side):
    """getSquareArea (number) -> (number)
    Return the area of a square based on side length"""
    area1 = side ** 2

    return area1



def getRectangleArea(width,height):
    """getRectangleArea -> width * height = area
    Uses 2 inputs to calculate area of rectangle."""
    area2 = width*height
    return area2










